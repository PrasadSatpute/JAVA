package com.example;

public class variableExample {
    public static void main(String[] args) {
        
        //Primitive type variable
        boolean paid = true;
        char ch = 'A';
        int ivar = 12;
        float fvar = 12.35f;
        double dvar = 12.35;
        System.out.println(paid);
        System.out.println(ch);
        System.out.println(ivar);
        System.out.println(fvar);
        System.out.println(dvar);
        
        
        
    }
    
}
